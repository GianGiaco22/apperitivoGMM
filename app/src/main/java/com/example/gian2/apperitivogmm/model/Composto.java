package com.example.gian2.apperitivogmm.model;

public class Composto {
    private String pietanza;
    private int quantita;
    private String modifica;
    private int codiceOrdine;

    public String getPietanza() {
        return pietanza;
    }

    public void setPietanza(String pietanza) {
        this.pietanza = pietanza;
    }

    public int getQuantita() {
        return quantita;
    }

    public void setQuantita(int quantita) {
        this.quantita = quantita;
    }

    public String getModifica() {
        return modifica;
    }

    public void setModifica(String modifica) {
        this.modifica = modifica;
    }

    public int getCodiceOrdine() {
        return codiceOrdine;
    }

    public void setCodiceOrdine(int codiceOrdine) {
        this.codiceOrdine = codiceOrdine;
    }
}
